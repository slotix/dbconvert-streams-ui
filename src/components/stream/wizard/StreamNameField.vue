<template>
  <div class="space-y-3">
    <FormInput
      v-model="streamName"
      label="Stream Name"
      :placeholder="autoGeneratedName || 'Enter custom name or leave blank for auto-generation'"
      :helper-text="
        !streamName && autoGeneratedName
          ? `Auto-generated name: ${autoGeneratedName}`
          : 'Leave blank for auto-generated name or enter a custom name.'
      "
    />

    <div>
      <label
        for="stream-description"
        class="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-1"
      >
        Description
        <span class="text-gray-400 dark:text-gray-500">(Optional)</span>
      </label>
      <textarea
        id="stream-description"
        v-model="streamDescription"
        rows="2"
        maxlength="500"
        class="ui-accent-focus ui-surface-raised ui-border-default block w-full rounded-md border text-gray-900 shadow-sm transition-colors duration-150 placeholder-gray-400 focus:outline-none sm:text-sm dark:text-gray-100 dark:placeholder:text-gray-500"
        placeholder="Add context for this stream (purpose, dataset, owner, notes)"
      />
      <p class="mt-1 text-xs text-gray-500 dark:text-gray-400">
        Helps identify streams in search and details view.
      </p>
    </div>
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue'
import { useStreamsStore } from '@/stores/streamConfig'
import FormInput from '@/components/base/FormInput.vue'
import { getFileSpec } from '@/composables/useTargetSpec'

interface Props {
  sourceConnectionId?: string | null
  targetConnectionId?: string | null
  sourceDatabase?: string | null
  targetDatabase?: string | null
}

const props = withDefaults(defineProps<Props>(), {
  sourceConnectionId: null,
  targetConnectionId: null,
  sourceDatabase: null,
  targetDatabase: null
})

const streamsStore = useStreamsStore()

const streamName = computed({
  get: () => streamsStore.currentStreamConfig?.name || '',
  set: (value) => {
    if (streamsStore.currentStreamConfig) {
      streamsStore.currentStreamConfig.name = value
    }
  }
})

const streamDescription = computed({
  get: () => streamsStore.currentStreamConfig?.description || '',
  set: (value) => {
    if (!streamsStore.currentStreamConfig) return
    streamsStore.currentStreamConfig.description = value
  }
})

const autoGeneratedName = computed(() => {
  if (!props.targetConnectionId) {
    return ''
  }

  // Use all source connections for name generation (federated mode support)
  const sourceConnections = streamsStore.currentStreamConfig?.source?.connections || []
  if (sourceConnections.length === 0) {
    return ''
  }

  const targetFileFormat = getFileSpec(streamsStore.currentStreamConfig?.target?.spec)?.fileFormat

  return streamsStore.generateDefaultStreamConfigName(
    sourceConnections,
    props.targetConnectionId,
    targetFileFormat
  )
})
</script>
